    public void sevenDays()
    {
        
        Calendar cal = Calendar.getInstance();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        cal.add(Calendar.DATE, -7);
        String then = sdf.format(cal.getTime());
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDateTime now = LocalDateTime.now();
        String cd = dtf.format(now);
        
        
        
        
        String cname = combo_course.getSelectedItem().toString();
        Float totalAmount = 0.0f;
        
        try {
            DefaultTableModel model;
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/fees_management","root","");
            PreparedStatement pst=Con.prepareStatement("select * from fees_details where date between ? and ? and courses = ? ");
            pst.setString(1, then);
            pst.setString(2,  cd);
            pst.setString(3, cname);
            ResultSet rs = pst.executeQuery();
            
            while (rs.next())
            {
                String recieptNo = rs.getString("recepit_no");
                String rollNo = rs.getString("roll_no");
                String studentName = rs.getString("student_name");
                String courseName = rs.getString("courses");
                float amount = rs.getFloat("total_amount");
                String paymentMode = rs.getString("payment_mode");
                String date = rs.getString("date");
                String remark = rs.getString("remark");
            
                Object[] obj = {recieptNo,rollNo,studentName,courseName,amount,paymentMode,date,remark};
                model = (DefaultTableModel)tbl_viewrecord.getModel();
                model.addRow(obj);
                
                
                totalAmount = totalAmount + amount;
            }    
                lbl_amount_collected.setText(totalAmount.toString());
                lbl_selectcourse.setText(cname);
                lbl_words.setText(NumberToWordsConverter.convert(totalAmount.intValue()));
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
