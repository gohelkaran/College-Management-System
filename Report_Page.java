    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        clearTable();
        switch (combo_lastday.getSelectedIndex()) {
            case 0:
                clearTable();
                sevenDays();
                break;
            case 1:
                clearTable();
                oneMonth();
                break;
            case 2:
                clearTable();
                sixMonth();
                break;
            case 3:
                clearTable();
                oneYear();
                break;
            case 4:
                clearTable();
                threeYear();
                break;
            case 5:
                clearTable();
                sixYear();
                break;
            default:
                JOptionPane.showMessageDialog(this, "Invalid Selection");
                break;
        }
    }//GEN-LAST:event_btn_submitActionPerformed

